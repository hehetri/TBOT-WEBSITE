const langToggle = document.getElementById('langToggle');
let currentLang = localStorage.getItem('bawoo-lang') || 'en';

function applyTranslations(lang) {
  const dict = translations[lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const path = el.dataset.i18n.split('.');
    let ref = dict;
    for (const key of path) {
      ref = ref?.[key];
    }
    if (typeof ref === 'string') {
      el.textContent = ref;
    }
  });
  currentLang = lang;
  localStorage.setItem('bawoo-lang', lang);
  if (langToggle) langToggle.textContent = lang === 'en' ? 'EN / PT' : 'PT / EN';
}

if (langToggle) {
  langToggle.addEventListener('click', () => {
    applyTranslations(currentLang === 'en' ? 'pt' : 'en');
  });
}

document.addEventListener('DOMContentLoaded', () => applyTranslations(currentLang));
